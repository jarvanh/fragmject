package com.example.miaow.base.utils

import androidx.annotation.VisibleForTesting
import java.io.File
import java.nio.charset.Charset

/**
 * FileUtil 拆分文件之五：磁盘脏数据写入测试。
 *
 * 拆分动机：原本写在 [FileUtil] 里的硬编码古文（≈ 300 行）使主文件
 * 体积达到 59 KB，与文件工具的核心职责无关。这里独立成文，方便日后
 * 替换为更小的 lorem ipsum 或 assets 资源。
 */
@VisibleForTesting
internal fun writeDirtyReadInternal(destFile: File) {
    Thread {
        while (true) {
            if (getAvailableStorageInternal() > 1000 * 1000) {
                writeToFileInternal(
                    DIRTY_READ_TEXT,
                    Charset.defaultCharset(),
                    destFile,
                    true
                )
            }
        }
    }.start()
}

